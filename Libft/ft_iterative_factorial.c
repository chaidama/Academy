int		ft_iterative_factorial(int nb)
{
	int x;

	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	x = 1;
	while (nb > 0)
	{
		x = x * nb;
		nb--;
	}
	return (x);
}
